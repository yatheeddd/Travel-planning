const jwt = require('jsonwebtoken');

/**
 * JWT authentication middleware.
 * We use JWT because it provides a stateless way to authenticate API requests.
 * The token is signed with a secret and includes the user id and email.
 * The middleware verifies the token and attaches user info to req.user.
 */
function authenticate(req, res, next) {
  const authHeader = req.headers.authorization;
  if (!authHeader || !authHeader.startsWith('Bearer ')) {
    return res.status(401).json({ message: 'Missing or invalid authorization header' });
  }

  const token = authHeader.split(' ')[1];
  try {
    const payload = jwt.verify(token, process.env.JWT_SECRET);
    req.user = { id: payload.id, email: payload.email };
    next();
  } catch (err) {
    return res.status(401).json({ message: 'Invalid or expired token' });
  }
}

module.exports = { authenticate };
